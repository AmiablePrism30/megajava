package Day01;

public class Day01_2 {
	
	public static void main(String[] args) {
		
		System.out.println("print on console");
		
		System.out.println(123);
		System.out.println("java");
		System.out.println("java");
		System.out.println("java123");
		
		System.out.println("word print 1");// 출력후 자동 줄바꿈
		System.out.print("word print 2\n"); 		/// 출력
		System.out.printf("%s","word print 3\n"); 		// 출력 포맷 가능 출력물 꾸미기 가능
		System.out.printf("My age is %d and MY favorite number is %d\n",40,7);
		System.out.printf("My age is "+40+" and My Favorite number is "+7+"\n");
		System.out.printf("Float is %f\n",10.5);
		System.out.printf("Character is %c\n",'A');
		System.out.printf("String is %s\n","Hello");
		// \r : 다음줄의  첫칸로 이동
		
		// \\   \" \'
		
		// 출석부 만들기
		System.out.println("\t\t[[[[[[Attendance]]]]]]");
		System.out.println("Name\tfirst class\tsecond class\tthrid class");
		System.out.println("James \t present \t absent \t present");
		System.out.println("Amy \t absent \t present \t present");
		System.out.println("-----------------------------------------------------");
		
		
		
		
		
		
	}

}
